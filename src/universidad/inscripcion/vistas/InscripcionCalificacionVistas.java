    private void CBInscripcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CBInscripcionActionPerformed
        // TODO add your handling code here:
        if(CBInscripcion.getSelectedItem().toString().equalsIgnoreCase("Inscripcion")){
            TXTAlumno.setText("");
            TXTMateria.setText("");
            TXTCalificacion.setText("");
        }else{
       String arr[]= CBInscripcion.getSelectedItem().toString().split("-");
       TXTIdInscripcion.setText(arr[0]);
       TXTAlumno.setText(arr[1]);
       TXTMateria.setText(arr[2]);
       int a=Integer.parseInt(arr[0]);
       Inscripcion in=id.buscarInscripcion(a);
       double cal=in.getNota();
       String c=String.valueOf(cal);
       TXTCalificacion.setText(c);
       }
    }//GEN-LAST:event_CBInscripcionActionPerformed

    private void BCalificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCalificacionActionPerformed
        // TODO add your handling code here:
       double n=Double.parseDouble(TXTCalificacion.getText());
        if(n<=10){
        int a=Integer.parseInt(TXTIdInscripcion.getText());
        Inscripcion ins=id.buscarInscripcion(a);
        ins.setNota(n);
        id.actualizarInscripcion(ins);
        JOptionPane.showMessageDialog(this, "Calificacion guardada");

        }else{
            JOptionPane.showMessageDialog(this, "por favor ingrese una calificacion menor a 10");
        }
    }//GEN-LAST:event_BCalificacionActionPerformed

