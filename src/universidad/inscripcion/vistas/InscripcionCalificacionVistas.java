    public InscripcionCalificacionVistas() {
        initComponents();
        ArrayList<Inscripcion> arr = new ArrayList<>();
        arr.addAll(id.obtenerInscripcion());
        for(int x=0;x<arr.size();x++){
            String idI=String.valueOf(arr.get(x).getIdInscripcion());
            int a=arr.get(x).getAlumno().getIdAlumno();
            int m=arr.get(x).getMateria().getIdMateria();
            Alumno al=ad.buscarAlumno(a);
            Materia ma=md.buscarMateria(m);
            String cb=idI+"-"+al.getNombreAlumno()+"-"+ma.getNombreMateria();
            CBInscripcion.addItem(cb);
        }
    }

